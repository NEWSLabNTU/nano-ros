struct SubscriberSlot {
    data: [u8; BUFFER_SIZE],
    has_data: AtomicBool,
    len: AtomicUsize,
    overflow: AtomicBool,
    /// Flag indicating a reader is currently accessing this slot.
    /// The callback checks this flag and drops the message if locked,
    /// preventing a data race where the callback overwrites the buffer mid-read.
    locked: AtomicBool,
    waker: AtomicWaker,
    datareader_id: u16,
    active: bool,
}

impl SubscriberSlot {
    const fn new() -> Self {
        Self {
            data: [0u8; BUFFER_SIZE],
            has_data: AtomicBool::new(false),
            len: AtomicUsize::new(0),
            overflow: AtomicBool::new(false),
            locked: AtomicBool::new(false),
            waker: AtomicWaker::new(),
            datareader_id: 0,
            active: false,
        }
    }
}

static mut SUBSCRIBER_SLOTS: [SubscriberSlot; MAX_SUBSCRIBERS] =
    [const { SubscriberSlot::new() }; MAX_SUBSCRIBERS];

