/// XRCE-DDS publisher handle.
pub struct XrcePublisher {
    datawriter_id: uxrObjectId,
}

impl Publisher for XrcePublisher {
    type Error = TransportError;

    fn publish_raw(&self, data: &[u8]) -> Result<(), TransportError> {
        ffi_guard(|| unsafe {
            // Try the non-fragmented fast path first.
            let req = xrce_sys::uxr_buffer_topic(
                &raw mut SESSION,
                OUTPUT_RELIABLE,
                self.datawriter_id,
                data.as_ptr() as *mut u8,
                data.len(),
            );
            if req != xrce_sys::UXR_INVALID_REQUEST_ID {
                // Flush the output stream so the data reaches the agent.
                // Without this, data stays in the reliable stream buffer
                // until the next uxr_run_session_time() call (e.g., from
                // spin_once). Callers that don't use an executor (e.g.,
                // the C API's nros_publish_raw loop) would never flush.
                xrce_sys::uxr_run_session_time(&raw mut SESSION, 0);
                return Ok(());
            }

            // Fast path failed (message too large for single stream slot).
            // Fall back to fragmented output stream.
            let mut ub = core::mem::zeroed::<xrce_sys::ucdrBuffer>();
            let req = xrce_sys::uxr_prepare_output_stream_fragmented(
                &raw mut SESSION,
                OUTPUT_RELIABLE,
                self.datawriter_id,
                &raw mut ub,
                data.len(),
                Some(flush_output_streams),
                &raw mut SESSION as *mut core::ffi::c_void,
            );
            if req == xrce_sys::UXR_INVALID_REQUEST_ID {
                return Err(TransportError::PublishFailed);
            }

            // Serialize the payload into the fragmented stream. The ucdrBuffer's
            // on_full_buffer callback handles flushing and advancing to the next
            // fragment automatically.
            let ok = xrce_sys::ucdr_serialize_array_uint8_t(&raw mut ub, data.as_ptr(), data.len());
            if !ok {
                return Err(TransportError::PublishFailed);
            }

            // Flush the final fragment.
            xrce_sys::uxr_flash_output_streams(&raw mut SESSION);

            Ok(())
        })
    }

    fn buffer_error(&self) -> TransportError {
        TransportError::BufferTooSmall
    }

    fn serialization_error(&self) -> TransportError {
        TransportError::SerializationError
    }
}

