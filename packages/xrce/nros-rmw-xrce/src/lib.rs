/// Timeout for `uxr_run_session_time` flush calls (milliseconds).
const SESSION_FLUSH_TIMEOUT_MS: c_int = 100;

/// Retries for `uxr_create_session_retries`.
const SESSION_CREATION_RETRIES: usize = 10;

/// Stack buffer size for DDS topic/type/service names.
const DDS_NAME_BUF_SIZE: usize = 128;

/// Stack buffer size for participant name.
const PARTICIPANT_NAME_BUF_SIZE: usize = 64;

/// Flush callback for `uxr_prepare_output_stream_fragmented`.
///
/// Called by the XRCE library when the output stream buffer is full during
/// fragmented writes. Flushes pending fragments to the transport and runs
/// the session briefly to process acknowledgements.
unsafe extern "C" fn flush_output_streams(
    session: *mut xrce_sys::uxrSession,
    _args: *mut core::ffi::c_void,
) -> bool {
    unsafe {
        xrce_sys::uxr_flash_output_streams(session);
        xrce_sys::uxr_run_session_time(session, SESSION_FLUSH_TIMEOUT_MS)
    }
}

