    fn close(&mut self) -> Result<(), TransportError> {
        ffi_guard(|| unsafe {
            if INITIALIZED {
                xrce_sys::uxr_delete_session(&raw mut SESSION);
                xrce_sys::uxr_close_custom_transport(&raw mut TRANSPORT);

                // Reset all state
                NEXT_ENTITY_ID = 2;
                INITIALIZED = false;
                for slot in &mut SUBSCRIBER_SLOTS {
                    slot.active = false;
                }
                for slot in &mut SERVICE_SERVER_SLOTS {
                    slot.active = false;
                }
                for slot in &mut SERVICE_CLIENT_SLOTS {
                    slot.active = false;
                }
            }
            Ok(())
        })
    }

    fn drive_io(&mut self, timeout_ms: i32) -> Result<(), Self::Error> {
        self.spin_once(timeout_ms);
        Ok(())
    }
