/// XRCE-DDS subscriber handle.
pub struct XrceSubscriber {
    slot_index: usize,
}

impl Subscriber for XrceSubscriber {
    type Error = TransportError;

    fn has_data(&self) -> bool {
        ffi_guard(|| unsafe {
            SUBSCRIBER_SLOTS[self.slot_index]
                .has_data
                .load(Ordering::Acquire)
        })
    }

    fn try_recv_raw(&mut self, buf: &mut [u8]) -> Result<Option<usize>, TransportError> {
        ffi_guard(|| unsafe {
            let slot = &SUBSCRIBER_SLOTS[self.slot_index];

            if !slot.has_data.load(Ordering::Acquire) {
                return Ok(None);
            }

            // Check for overflow (message too large for slot buffer)
            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);
            if len > buf.len() {
                // User buffer too small — clear the slot and report error
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::BufferTooSmall);
            }

            // Lock slot to prevent callback from overwriting during copy
            slot.locked.store(true, Ordering::Release);
            buf[..len].copy_from_slice(&slot.data[..len]);
            slot.locked.store(false, Ordering::Release);
            slot.has_data.store(false, Ordering::Release);
            Ok(Some(len))
        })
    }

    fn process_raw_in_place(&mut self, f: impl FnOnce(&[u8])) -> Result<bool, TransportError> {
        ffi_guard(|| unsafe {
            let slot = &SUBSCRIBER_SLOTS[self.slot_index];

            if !slot.has_data.load(Ordering::Acquire) {
                return Ok(false);
            }

            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);

            // Lock slot, process in-place, then unlock
            slot.locked.store(true, Ordering::Release);
            f(&SUBSCRIBER_SLOTS[self.slot_index].data[..len]);
            slot.locked.store(false, Ordering::Release);
            slot.has_data.store(false, Ordering::Release);

            Ok(true)
        })
    }

    fn register_waker(&self, waker: &core::task::Waker) {
        unsafe {
            SUBSCRIBER_SLOTS[self.slot_index].waker.register(waker);
        }
    }

    /// Returns `(len, None)` — XRCE does not yet plumb per-sample info
    /// through the receive callback, so the returned `MessageInfo` is
    /// always `None`. The zenoh backend parses RMW attachments to
    /// populate publisher GID / sequence number / source timestamp;
    /// adding equivalent support on XRCE (reading `uxrObjectId` /
    /// sample identity from `uxr_run_session`) is tracked as a
    /// follow-up to Phase 84.E1. Users that need `MessageInfo` should
    /// stick with the zenoh backend for now.
    fn try_recv_raw_with_info(
        &mut self,
        buf: &mut [u8],
    ) -> Result<Option<(usize, Option<nros_rmw::MessageInfo>)>, TransportError> {
        // Explicit override (not a default fallthrough) so the gap is
        // visible in the code; behavior matches the trait default.
        self.try_recv_raw(buf).map(|opt| opt.map(|len| (len, None)))
    }

    fn deserialization_error(&self) -> TransportError {
        TransportError::DeserializationError
    }
}

