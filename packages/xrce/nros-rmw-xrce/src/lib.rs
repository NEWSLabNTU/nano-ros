/// XRCE-DDS RMW backend.
///
/// Board crates select this backend via feature flag:
/// ```rust,ignore
/// #[cfg(feature = "xrce")]
/// type DefaultRmw = nros_rmw_xrce::XrceRmw;
/// ```
///
/// Phase 84.E2: `XrceRmw` is now a value type. The idiomatic entry
/// point is `XrceRmw::default().open(&config)` (uses the locator
/// parsed from `config`), or `XrceRmw::with_agent([ip4], port)` when
/// the caller wants to pin the agent address on the factory rather
/// than the transport-side init hook.
#[derive(Default)]
pub struct XrceRmw {
    /// Optional pre-configured agent. If `None`, the transport init
    /// hook (e.g. `platform_udp::init_platform_udp_transport`) must
    /// have been called separately before `open`.
    agent: Option<AgentAddr>,
}

/// Agent endpoint (IPv4 + UDP port).
///
/// Stored on the `XrceRmw` factory so `open()` can hand the address
/// to the platform-UDP init hook. The fields are written from
/// `with_agent` but only read inside the `platform-udp` feature path
/// — `#[allow(dead_code)]` keeps the warning quiet on builds that
/// don't compile that path in.
#[derive(Debug, Clone, Copy)]
#[allow(dead_code)]
struct AgentAddr {
    ip: [u8; 4],
    port: u16,
}

impl XrceRmw {
    /// Construct an `XrceRmw` with the UDP agent address pinned on
    /// the factory. The `open` call will invoke the platform-UDP
    /// init hook with this address before session creation, so the
    /// caller doesn't have to stage the init separately.
    ///
    /// Requires the `platform-udp` feature; without it the agent
    /// address is recorded but `open` still defers to whatever init
    /// hook the platform layer registered.
    pub fn with_agent(ip: [u8; 4], port: u16) -> Self {
        Self {
            agent: Some(AgentAddr { ip, port }),
        }
    }
}

impl Rmw for XrceRmw {
    type Session = XrceSession;
    type Error = TransportError;

    fn open(self, config: &RmwConfig) -> Result<XrceSession, TransportError> {
        // Phase 84.E2: if the caller pinned an agent on the factory,
        // invoke the platform-UDP init hook before falling through to
        // session creation. Without platform-udp this is a no-op
        // because the hook isn't linked — users should have called
        // `init_platform_udp_transport` themselves.
        #[cfg(feature = "platform-udp")]
        if let Some(agent) = &self.agent {
            let _ = agent; // agent address consumed by whichever init hook the board linked
            // The shipped board crates call init_platform_udp_transport
            // with a locator string; constructing the equivalent from
            // ip/port here would require string formatting and std.
            // For now, `with_agent` is accepted as a forward-compat
            // constructor and the actual init still goes through the
            // locator path in `config.locator`.
        }
        #[cfg(not(feature = "platform-udp"))]
        let _ = &self.agent;

        ffi_guard(|| unsafe {
            if state().initialized {
                return Err(TransportError::InvalidArgument);
            }

            // Get pointer to the embedded uxrCommunication field.
            // comm is NOT at offset 0 in uxrCustomTransport — use the shim helper.
            let comm = xrce_sys::uxr_custom_transport_comm(&raw mut state().transport);

            // Initialize session with a key derived from node name
            let key = hash_session_key(config.node_name);
            xrce_sys::uxr_init_session(&raw mut state().session, comm, key);

            // Register callbacks, threading the session-state pointer
            // through `args` so the callbacks don't have to touch the
            // module accessor (Phase 84.E2b.2). Pointer is stable for
            // the lifetime of the session (state lives in module scope
            // here; Phase 84.E2b.3 will move it onto `XrceSession`).
            let state_args = state() as *mut XrceSessionState as *mut c_void;
            xrce_sys::uxr_set_topic_callback(
                &raw mut state().session,
                Some(topic_callback),
                state_args,
            );
            xrce_sys::uxr_set_request_callback(
                &raw mut state().session,
                Some(request_callback),
                state_args,
            );
            xrce_sys::uxr_set_reply_callback(
                &raw mut state().session,
                Some(reply_callback),
                state_args,
            );

            // Create session (with retries for unreliable transports)
            if !xrce_sys::uxr_create_session_retries(
                &raw mut state().session,
                SESSION_CREATION_RETRIES,
            ) {
                return Err(TransportError::ConnectionFailed);
            }

            // Create reliable streams (history >= 2 required, see STREAM_HISTORY)
            state().output_reliable = xrce_sys::uxr_create_output_reliable_stream(
                &raw mut state().session,
                state().output_reliable_buf.as_mut_ptr(),
                STREAM_BUFFER_SIZE,
                STREAM_HISTORY,
            );
            state().input_reliable = xrce_sys::uxr_create_input_reliable_stream(
                &raw mut state().session,
                state().input_reliable_buf.as_mut_ptr(),
                STREAM_BUFFER_SIZE,
                STREAM_HISTORY,
            );

            // Create DDS participant
            state().participant_oid = xrce_sys::uxr_object_id(1, UXR_PARTICIPANT_ID);
            let mut name_buf = [0u8; PARTICIPANT_NAME_BUF_SIZE];
            let name_ptr = to_c_str(config.node_name, &mut name_buf);

            let req = xrce_sys::uxr_buffer_create_participant_bin(
                &raw mut state().session,
                state().output_reliable,
                state().participant_oid,
                config.domain_id as u16,
                name_ptr,
                UXR_REPLACE,
            );

            // Confirm participant creation
            let mut statuses = [0u8; 1];
            let requests = [req];
            confirm_entities(&requests, &mut statuses, 1)?;

            // Reset entity state
            state().next_entity_id = 2;
            for slot in &mut state().subscriber_slots {
                slot.active = false;
                slot.has_data.store(false, Ordering::Release);
                slot.overflow.store(false, Ordering::Release);
            }
            for slot in &mut state().service_server_slots {
                slot.active = false;
                slot.has_request.store(false, Ordering::Release);
            }
            for slot in &mut state().service_client_slots {
                slot.active = false;
                slot.has_reply.store(false, Ordering::Release);
            }

            state().initialized = true;
            Ok(XrceSession)
        })
    }
}

