/// XRCE-DDS service client handle.
pub struct XrceServiceClient {
    slot_index: usize,
    requester_id: uxrObjectId,
}

impl ServiceClientTrait for XrceServiceClient {
    type Error = TransportError;

    #[allow(deprecated)]
    fn call_raw(&mut self, request: &[u8], reply_buf: &mut [u8]) -> Result<usize, TransportError> {
        self.send_request_raw(request)?;

        // Wait for reply with retries. Each iteration uses a guarded
        // non-blocking spin to keep critical sections short.
        for _ in 0..SERVICE_REPLY_RETRIES {
            ffi_guard(|| unsafe {
                xrce_sys::uxr_run_session_time(&raw mut state().session, SERVICE_REPLY_TIMEOUT_MS);
            });

            if let Some(len) = self.try_recv_reply_raw(reply_buf)? {
                return Ok(len);
            }
        }

        Err(TransportError::Timeout)
    }

    fn send_request_raw(&mut self, request: &[u8]) -> Result<(), TransportError> {
        ffi_guard(|| unsafe {
            let slot = &state().service_client_slots[self.slot_index];

            // Clear any stale reply
            slot.has_reply.store(false, Ordering::Release);

            // Send request
            let req = xrce_sys::uxr_buffer_request(
                &raw mut state().session,
                state().output_reliable,
                self.requester_id,
                request.as_ptr() as *mut u8,
                request.len(),
            );
            if req == xrce_sys::UXR_INVALID_REQUEST_ID {
                return Err(TransportError::ServiceRequestFailed);
            }

            Ok(())
        })
    }

    fn try_recv_reply_raw(
        &mut self,
        reply_buf: &mut [u8],
    ) -> Result<Option<usize>, TransportError> {
        ffi_guard(|| unsafe {
            let slot = &state().service_client_slots[self.slot_index];

            if !slot.has_reply.load(Ordering::Acquire) {
                return Ok(None);
            }

            // Check for overflow (reply exceeded static buffer capacity)
            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_reply.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);
            if len > reply_buf.len() {
                slot.has_reply.store(false, Ordering::Release);
                return Err(TransportError::BufferTooSmall);
            }
            reply_buf[..len].copy_from_slice(&slot.data[..len]);
            slot.has_reply.store(false, Ordering::Release);
            Ok(Some(len))
        })
    }

    fn register_waker(&self, waker: &core::task::Waker) {
        unsafe {
            state().service_client_slots[self.slot_index]
                .waker
                .register(waker);
        }
    }
}
