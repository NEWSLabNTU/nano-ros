/// XRCE-DDS subscriber handle.
pub struct XrceSubscriber {
    slot_index: usize,
}

impl Subscriber for XrceSubscriber {
    type Error = TransportError;

    fn has_data(&self) -> bool {
        ffi_guard(|| unsafe {
            state().subscriber_slots[self.slot_index]
                .has_data
                .load(Ordering::Acquire)
        })
    }

    fn try_recv_raw(&mut self, buf: &mut [u8]) -> Result<Option<usize>, TransportError> {
        ffi_guard(|| unsafe {
            let slot = &state().subscriber_slots[self.slot_index];

            if !slot.has_data.load(Ordering::Acquire) {
                return Ok(None);
            }

            // Check for overflow (message too large for slot buffer)
            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);
            if len > buf.len() {
                // User buffer too small — clear the slot and report error
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::BufferTooSmall);
            }

            // Lock slot to prevent callback from overwriting during copy
            slot.locked.store(true, Ordering::Release);
            buf[..len].copy_from_slice(&slot.data[..len]);
            slot.locked.store(false, Ordering::Release);
            slot.has_data.store(false, Ordering::Release);
            Ok(Some(len))
        })
    }

    fn process_raw_in_place(&mut self, f: impl FnOnce(&[u8])) -> Result<bool, TransportError> {
        ffi_guard(|| unsafe {
            let slot = &state().subscriber_slots[self.slot_index];

            if !slot.has_data.load(Ordering::Acquire) {
                return Ok(false);
            }

            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);

            // Lock slot, process in-place, then unlock
            slot.locked.store(true, Ordering::Release);
            f(&state().subscriber_slots[self.slot_index].data[..len]);
            slot.locked.store(false, Ordering::Release);
            slot.has_data.store(false, Ordering::Release);

            Ok(true)
        })
    }

    fn register_waker(&self, waker: &core::task::Waker) {
        unsafe {
            state().subscriber_slots[self.slot_index]
                .waker
                .register(waker);
        }
    }

    /// Returns `(len, None)` — XRCE does not yet plumb per-sample info
    /// through the receive callback, so the returned `MessageInfo` is
    /// always `None`. The zenoh backend parses RMW attachments to
    /// populate publisher GID / sequence number / source timestamp;
    /// adding equivalent support on XRCE (reading `uxrObjectId` /
    /// sample identity from `uxr_run_session`) is tracked as a
    /// follow-up to Phase 84.E1. Users that need `MessageInfo` should
    /// stick with the zenoh backend for now.
    fn try_recv_raw_with_info(
        &mut self,
        buf: &mut [u8],
    ) -> Result<Option<(usize, Option<nros_rmw::MessageInfo>)>, TransportError> {
        // Explicit override (not a default fallthrough) so the gap is
        // visible in the code; behavior matches the trait default.
        self.try_recv_raw(buf).map(|opt| opt.map(|len| (len, None)))
    }

    fn deserialization_error(&self) -> TransportError {
        TransportError::DeserializationError
    }
}

// ============================================================================
// Phase 99.G — XrceSubscriber SlotBorrowing (zero-copy receive)
// ============================================================================

/// Backend-lent read-only view into the subscriber slot's static receive
/// buffer. Holds the slot's `locked` flag for the lifetime of the view
/// so the XRCE notify callback can't overwrite the bytes while the user
/// is reading them. On drop the lock is released and `has_data` cleared
/// (consume-on-borrow semantics, matching `try_recv_raw`).
#[cfg(feature = "lending")]
pub struct XrceView<'a> {
    bytes: &'a [u8],
    slot: &'a SubscriberSlot,
}

#[cfg(feature = "lending")]
impl<'a> AsRef<[u8]> for XrceView<'a> {
    fn as_ref(&self) -> &[u8] {
        self.bytes
    }
}

#[cfg(feature = "lending")]
impl<'a> Drop for XrceView<'a> {
    fn drop(&mut self) {
        // Release lock first so it stays consistent with has_data.
        self.slot.locked.store(false, Ordering::Release);
        self.slot.has_data.store(false, Ordering::Release);
    }
}

#[cfg(feature = "lending")]
impl nros_rmw::SlotBorrowing for XrceSubscriber {
    type View<'a>
        = XrceView<'a>
    where
        Self: 'a;

    fn try_borrow(&mut self) -> Result<Option<Self::View<'_>>, TransportError> {
        ffi_guard(|| {
            // SAFETY: state() returns &mut to a 'static singleton; the
            // slot index was bounds-checked at subscriber registration.
            let slot: &SubscriberSlot = unsafe { &state().subscriber_slots[self.slot_index] };

            if !slot.has_data.load(Ordering::Acquire) {
                return Ok(None);
            }
            if slot.overflow.load(Ordering::Acquire) {
                slot.overflow.store(false, Ordering::Release);
                slot.has_data.store(false, Ordering::Release);
                return Err(TransportError::MessageTooLarge);
            }

            let len = slot.len.load(Ordering::Acquire);

            // Lock against the C callback before borrowing.
            slot.locked.store(true, Ordering::Release);

            // SAFETY: slot.data is valid for `BUFFER_SIZE`; len ≤ BUFFER_SIZE
            // (notify callback drops messages that don't fit). locked=true
            // gates the callback from overwriting until XrceView::drop.
            let bytes = unsafe { core::slice::from_raw_parts(slot.data.as_ptr(), len) };

            Ok(Some(XrceView { bytes, slot }))
        })
    }
}

