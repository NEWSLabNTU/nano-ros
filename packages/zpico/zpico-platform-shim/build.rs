//! Build script for zpico-platform-shim.
//!
//! Probes the sizes of zenoh-pico's `_z_sys_net_socket_t` and
//! `_z_sys_net_endpoint_t` from the C headers using the cross-compiler,
//! then generates Rust constants for the opaque wrapper types.

use std::env;
use std::path::{Path, PathBuf};
use std::process::Command;

fn main() {
    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").unwrap());
    let zpico_sys_dir = manifest_dir.parent().unwrap().join("zpico-sys");
    let zenoh_pico_include = zpico_sys_dir.join("zenoh-pico/include");

    // Default sizes (used when no platform is selected or probe fails)
    let mut socket_size: usize = 16;
    let mut endpoint_size: usize = 8;

    // Only probe if zenoh-pico headers exist
    if zenoh_pico_include.exists() {
        if let Some((s, e)) = probe_sizes(&zpico_sys_dir, &zenoh_pico_include, &out_dir) {
            socket_size = s;
            endpoint_size = e;
        }
    }

    let contents = format!(
        "// Auto-generated by zpico-platform-shim/build.rs\n\
         // Sizes derived from zenoh-pico C headers via size probe.\n\
         pub const NET_SOCKET_SIZE: usize = {socket_size};\n\
         pub const NET_ENDPOINT_SIZE: usize = {endpoint_size};\n",
    );
    std::fs::write(out_dir.join("platform_net_sizes.rs"), contents).unwrap();

    println!("cargo:rerun-if-changed=build.rs");
    println!(
        "cargo:rerun-if-changed={}",
        zpico_sys_dir.join("c/size_probe.c").display()
    );
}

fn probe_sizes(
    zpico_sys_dir: &Path,
    zenoh_include: &Path,
    out_dir: &Path,
) -> Option<(usize, usize)> {
    let probe_c = zpico_sys_dir.join("c/size_probe.c");
    if !probe_c.exists() {
        return None;
    }

    let mut build = cc::Build::new();
    build.file(&probe_c);
    build.include(zenoh_include);

    let target = env::var("TARGET").unwrap_or_default();

    if env::var("FREERTOS_DIR").is_ok() {
        build.define("ZENOH_FREERTOS_LWIP", None);
        if let Ok(dir) = env::var("FREERTOS_DIR") {
            let f = PathBuf::from(&dir);
            build.include(f.join("include"));
            if let Ok(port) = env::var("FREERTOS_PORT") {
                build.include(f.join("portable").join(&port));
            }
        }
        if let Ok(dir) = env::var("FREERTOS_CONFIG_DIR") {
            build.include(dir);
        }
        if let Ok(dir) = env::var("LWIP_DIR") {
            build.include(PathBuf::from(dir).join("src/include"));
        }
    } else if env::var("NUTTX_DIR").is_ok() {
        build.define("ZENOH_NUTTX", None);
        build.define("ZENOH_LINUX", None);
        if let Ok(dir) = env::var("NUTTX_DIR") {
            build.include(PathBuf::from(dir).join("include"));
        }
    } else if env::var("THREADX_DIR").is_ok() {
        build.define("ZENOH_GENERIC", None);
        build.define("ZENOH_THREADX", None);
        build.include(zpico_sys_dir.join("c/platform"));
    } else if target.contains("none") {
        // Bare-metal: void.h
    } else if target.contains("linux") {
        build.define("ZENOH_LINUX", None);
    } else if target.contains("darwin") || target.contains("macos") {
        build.define("ZENOH_MACOS", None);
    }

    build.cargo_metadata(false);
    build.try_compile("nros_size_probe").ok()?;

    let archive = out_dir.join("libnros_size_probe.a");
    let socket = read_symbol_size(&archive, "__nros_sizeof_net_socket");
    let endpoint = read_symbol_size(&archive, "__nros_sizeof_net_endpoint");

    if socket > 0 && endpoint > 0 {
        Some((socket, endpoint))
    } else {
        None
    }
}

fn read_symbol_size(archive: &Path, symbol: &str) -> usize {
    let sysroot = Command::new("rustc")
        .args(["--print", "sysroot"])
        .output()
        .ok()
        .map(|o| String::from_utf8_lossy(&o.stdout).trim().to_string())
        .unwrap_or_default();

    let host = env::var("HOST").unwrap_or_default();
    let rust_nm = PathBuf::from(&sysroot)
        .join("lib/rustlib")
        .join(&host)
        .join("bin/llvm-nm");

    for nm in &[rust_nm, PathBuf::from("llvm-nm"), PathBuf::from("nm")] {
        if let Ok(output) = Command::new(nm)
            .args(["--print-size", "--defined-only"])
            .arg(archive)
            .output()
        {
            if output.status.success() {
                let stdout = String::from_utf8_lossy(&output.stdout);
                for line in stdout.lines() {
                    if line.contains(symbol) {
                        let parts: Vec<&str> = line.split_whitespace().collect();
                        if parts.len() >= 2 {
                            if let Ok(size) = usize::from_str_radix(parts[1], 16) {
                                return size;
                            }
                        }
                    }
                }
            }
        }
    }
    0
}
