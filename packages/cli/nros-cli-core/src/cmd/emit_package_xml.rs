// Hand-rolled XML emitter
// ---------------------------------------------------------------------------

struct RenderInputs<'a> {
    pkg_name: &'a str,
    version: &'a str,
    description: &'a str,
    license: &'a str,
    /// Phase 212.B.4 — when present, replaces the default
    /// `Developer <dev@example.com>` placeholder maintainer.
    maintainer: Option<&'a AmentMaintainer>,
    build_depend: Vec<String>,
    /// Phase 212.B.4 — explicit `<buildtool_depend>` rows.
    buildtool_depend: Vec<String>,
    exec_depend: Vec<String>,
    test_depend: Vec<String>,
    build_type: &'a str,
}

fn render_xml(inp: RenderInputs<'_>) -> String {
    let mut out = String::new();
    out.push_str("<?xml version=\"1.0\"?>\n");
    out.push_str(GENERATED_HEADER_COMMENT);
    out.push('\n');
    out.push_str("<package format=\"3\">\n");
    push_text_elem(&mut out, "name", inp.pkg_name, 2);
    push_text_elem(&mut out, "version", inp.version, 2);
    push_text_elem(&mut out, "description", inp.description, 2);
    push_maintainer(&mut out, 2, inp.maintainer);
    push_text_elem(&mut out, "license", inp.license, 2);

    if !inp.buildtool_depend.is_empty() {
        out.push('\n');
        for d in &inp.buildtool_depend {
            push_text_elem(&mut out, "buildtool_depend", d, 2);
        }
    }
    if !inp.build_depend.is_empty() {
        out.push('\n');
        for d in &inp.build_depend {
            push_text_elem(&mut out, "build_depend", d, 2);
        }
    }
    if !inp.exec_depend.is_empty() {
        out.push('\n');
        for d in &inp.exec_depend {
            push_text_elem(&mut out, "exec_depend", d, 2);
        }
    }
    if !inp.test_depend.is_empty() {
        out.push('\n');
        for d in &inp.test_depend {
            push_text_elem(&mut out, "test_depend", d, 2);
        }
    }

    out.push('\n');
    out.push_str("  <export>\n");
    push_text_elem(&mut out, "build_type", inp.build_type, 4);
    out.push_str("  </export>\n");
    out.push_str("</package>\n");
    out
}

fn push_maintainer(out: &mut String, indent: usize, m: Option<&AmentMaintainer>) {
    for _ in 0..indent {
        out.push(' ');
    }
    let (name, email) = match m {
        Some(m) => (m.name.as_str(), m.email.as_str()),
        None => (DEFAULT_MAINTAINER_NAME, DEFAULT_MAINTAINER_EMAIL),
    };
    out.push_str("<maintainer email=\"");
    push_attr_escaped(out, email);
    out.push_str("\">");
    push_text_escaped(out, name);
    out.push_str("</maintainer>\n");
}

fn push_text_elem(out: &mut String, tag: &str, body: &str, indent: usize) {
    for _ in 0..indent {
        out.push(' ');
    }
    out.push('<');
    out.push_str(tag);
    out.push('>');
    push_text_escaped(out, body);
    out.push_str("</");
    out.push_str(tag);
    out.push_str(">\n");
}

fn push_text_escaped(out: &mut String, body: &str) {
    for ch in body.chars() {
        match ch {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '>' => out.push_str("&gt;"),
            _ => out.push(ch),
        }
    }
}

fn push_attr_escaped(out: &mut String, body: &str) {
    for ch in body.chars() {
        match ch {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '"' => out.push_str("&quot;"),
            _ => out.push(ch),
        }
    }
}

fn sorted_unique(src: &[String]) -> Vec<String> {
    let set: BTreeSet<&String> = src.iter().collect();
    set.into_iter().cloned().collect()
}

// ---------------------------------------------------------------------------
// Drift detection helper for `nros check`
// ---------------------------------------------------------------------------

/// Phase 212.G.2 drift check. Re-renders `package.xml` for the given pkg dir
/// and compares it against the existing file (if present + generator-owned).
///
/// Returns:
/// * `Ok(None)` — file absent, or carries no generated marker (hand-written
///   package.xml — out of scope for drift; user owns it).
/// * `Ok(Some(())) ` — file matches a fresh emit. No drift.
/// * `Err(_)` — file carries the marker but DIFFERS from a fresh emit:
///   the diagnostic body describes the drift. Caller (`nros check`) prints
///   it as a warning.
pub fn check_drift(pkg_dir: &Path) -> Result<DriftStatus> {
    let on_disk_path = pkg_dir.join("package.xml");
    if !on_disk_path.exists() {
        return Ok(DriftStatus::Absent);
    }
    let on_disk = std::fs::read_to_string(&on_disk_path)
        .with_context(|| format!("read {}", on_disk_path.display()))?;
    if !on_disk.contains(GENERATED_HEADER_COMMENT) {
        return Ok(DriftStatus::HandWritten);
    }
    let fresh = render_for_pkg(pkg_dir)?;
    if on_disk == fresh {
        Ok(DriftStatus::Clean)
    } else {
        Ok(DriftStatus::Drift { on_disk_path })
    }
}

#[derive(Debug)]
pub enum DriftStatus {
    /// No `package.xml` on disk.
    Absent,
    /// Present but without the generated marker — user-maintained.
    HandWritten,
    /// Present, generator-owned, and matches a fresh emit.
    Clean,
    /// Present, generator-owned, but differs from a fresh emit.
    Drift { on_disk_path: PathBuf },
}

