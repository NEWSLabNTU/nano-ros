//! Internal `package.xml` helpers — Phase 212.G (CLI verb removed).
//!
//! Renders a `package.xml` from a component package's `Cargo.toml`
//! `[package.metadata.ament]` table, or from a bringup package's
//! `system.toml.[[component]]` block. The `nros emit package-xml`
//! user-facing verb was retired (users hand-write `package.xml` when
//! they need ament/colcon interop, or skip it entirely when they use
//! launch planning). The render path stays as an internal helper:
//!
//!   * `migrate::run` regenerates `package.xml` for every component
//!     during the pre-212 → post-212 sweep.
//!   * `check::run` uses [`check_drift`] to detect a hand-edited
//!     `package.xml` whose generator marker disagrees with the current
//!     `Cargo.toml` / `system.toml` (Phase 212.G.2).
//!
//! Nothing in this module is reachable from the CLI dispatcher.
//!
//! Two pkg shapes, dispatched by the artifacts present in `<pkg-dir>`:
//!
//! * **Component pkg** — `Cargo.toml` present, with
//!   `[package.metadata.nros.component]` (or `…components.<Name>`) and
//!   `[package.metadata.ament]`. Output carries `<build_depend>`,
//!   `<exec_depend>`, `<test_depend>`, and `<export><build_type>…</build_type></export>`.
//! * **Bringup pkg** — `system.toml` present. Output carries one
//!   `<exec_depend>` per `[system.components[*].pkg`, plus `<name>`/`<description>`
//!   derived from `[system].name`.
//!
//! Output discipline (every contract in §212.G):
//!
//! 1. A generated marker comment at the top so `nros check` can detect
//!    hand-edit drift later (§212.G.2 — implemented as a small `nros check`
//!    helper below; full lint surface is its own follow-up).
//! 2. Byte-identical idempotency on re-run with unchanged inputs.
//! 3. Alphabetised dependency entries so list ordering doesn't drift between
//!    `Cargo.toml` edits and emitted output.

use std::{
    collections::BTreeSet,
    path::{Path, PathBuf},
};

use eyre::{Context, Result, bail};
use serde::Deserialize;

use crate::orchestration::{
    ament::parse_ament_metadata,
    cargo_metadata_schema::{
        AmentMaintainer, PackageMetadataAment, PackageMetadataNros, SystemComponentEntry,
        SystemToml,
    },
};

/// Sentinel header injected at the top of every emitted `package.xml`.
///
/// `nros check` uses substring presence of this comment to decide a
/// `package.xml` is generator-owned (and therefore eligible for drift
/// diagnostics). Keep it stable — touching the wording invalidates every
/// previously-emitted file.
pub const GENERATED_HEADER_COMMENT: &str =
    "<!-- generated by nros emit package-xml; edits will be overwritten -->";

const DEFAULT_VERSION: &str = "0.0.0";
const DEFAULT_LICENSE: &str = "Apache-2.0";
const DEFAULT_MAINTAINER_EMAIL: &str = "dev@example.com";
const DEFAULT_MAINTAINER_NAME: &str = "Developer";
const DEFAULT_BUILD_TYPE_COMPONENT: &str = "ament_cargo";
const DEFAULT_BUILD_TYPE_BRINGUP: &str = "ament_cmake";
const DEFAULT_DESCRIPTION_BRINGUP_FMT: &str = "Generated bringup package for ";

// ---------------------------------------------------------------------------
// Dispatch
// ---------------------------------------------------------------------------

/// Decide which shape `<pkg-dir>` is and render the corresponding XML.
///
/// Order matters: a bringup pkg might end up in a cargo workspace's `src/`
/// tree with no `Cargo.toml` of its own; `system.toml` is the disambiguator.
pub fn render_for_pkg(pkg_dir: &Path) -> Result<String> {
    if !pkg_dir.exists() {
        bail!("pkg dir does not exist: {}", pkg_dir.display());
    }
    let cargo_toml = pkg_dir.join("Cargo.toml");
    let system_toml = pkg_dir.join("system.toml");

    if system_toml.exists() {
        return render_bringup(&system_toml);
    }
    if cargo_toml.exists() {
        return render_component(&cargo_toml);
    }
    bail!(
        "{}: neither Cargo.toml nor system.toml found — not a component or bringup pkg",
        pkg_dir.display()
    );
}

