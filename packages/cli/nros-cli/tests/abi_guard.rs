//! Phase 218.E — integration test for the ABI version guard between the
//! `nros` CLI binary and the runtime `nros-core` version a consumer's
//! `Cargo.lock` resolves.
//!
//! The fixture is a synthetic tempdir containing:
//! - `Cargo.lock` pinning `nros-core` at an obviously-wrong version
//!   (`0.0.999`) so the guard cannot possibly match the CLI's embedded
//!   `CARGO_PKG_VERSION`.
//! - `package.xml` so the verb has a valid manifest to walk up from.
//!
//! Two scenarios:
//! 1. `abi_guard_fires_on_mismatch` — running `nros generate-rust` against
//!    the fixture exits non-zero with a message naming both versions.
//! 2. `abi_guard_skipped_with_env_opt_out` — same fixture + `NROS_SKIP_
//!    VERSION_CHECK=1` bypasses the guard (verb proceeds; we only assert
//!    the mismatch message is *absent* from stderr — the actual codegen
//!    output may still fail downstream, e.g. on the synthetic empty
//!    `package.xml`, and we don't pin its exit code).

use std::{
    fs,
    path::{Path, PathBuf},
    process::Command,
    time::{SystemTime, UNIX_EPOCH},
};

const WRONG_VERSION: &str = "0.0.999";

fn temp_root(tag: &str) -> PathBuf {
    let stamp = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap()
        .as_nanos();
    let dir =
        std::env::temp_dir().join(format!("phase-218-e-{tag}-{}-{stamp}", std::process::id()));
    let _ = fs::remove_dir_all(&dir);
    fs::create_dir_all(&dir).unwrap();
    dir
}

fn write_fixture(root: &Path) {
    // Synthetic Cargo.lock pinning nros-core at a wrong version.
    fs::write(
        root.join("Cargo.lock"),
        format!(
            r#"# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "synthetic_consumer"
version = "0.0.1"
dependencies = ["nros-core"]

[[package]]
name = "nros-core"
version = "{WRONG_VERSION}"
"#
        ),
    )
    .unwrap();

    // Minimal package.xml so `generate-rust` accepts the manifest.
    fs::write(
        root.join("package.xml"),
        r#"<?xml version="1.0"?>
<package format="3">
  <name>abi_guard_fixture</name>
  <version>0.0.1</version>
  <description>Phase 218.E ABI guard test fixture.</description>
  <maintainer email="test@example.com">test</maintainer>
  <license>Apache-2.0</license>
  <buildtool_depend>ament_cargo</buildtool_depend>
  <export>
    <build_type>ament_cargo</build_type>
  </export>
</package>
"#,
    )
    .unwrap();
}

/// Path to the `nros` binary the test harness should invoke. Cargo sets
/// `CARGO_BIN_EXE_nros` when this test crate builds the `nros` binary as
/// part of its package — which it does, because `nros-cli` has
/// `[[bin]] name = "nros"`.
fn nros_bin() -> PathBuf {
    PathBuf::from(env!("CARGO_BIN_EXE_nros"))
}

#[test]
fn abi_guard_fires_on_mismatch() {
    let root = temp_root("guard_fires");
    write_fixture(&root);

    let output = Command::new(nros_bin())
        .arg("generate-rust")
        .arg("--manifest")
        .arg(root.join("package.xml"))
        .arg("--output")
        .arg(root.join("generated"))
        // Make sure no env-level opt-out is in scope.
        .env_remove("NROS_SKIP_VERSION_CHECK")
        .output()
        .expect("spawn nros");

    let stderr = String::from_utf8_lossy(&output.stderr);
    let stdout = String::from_utf8_lossy(&output.stdout);

    assert!(
        !output.status.success(),
        "expected non-zero exit; got success.\nstdout: {stdout}\nstderr: {stderr}",
    );
    let combined = format!("{stdout}\n{stderr}");
    assert!(
        combined.contains(WRONG_VERSION),
        "expected consumer version {WRONG_VERSION} in error output.\nstdout: {stdout}\nstderr: {stderr}",
    );
    assert!(
        combined.contains(env!("CARGO_PKG_VERSION")),
        "expected CLI version {} in error output.\nstdout: {stdout}\nstderr: {stderr}",
        env!("CARGO_PKG_VERSION"),
    );
    assert!(
        combined.contains("ABI version mismatch"),
        "expected the mismatch headline in error output.\nstdout: {stdout}\nstderr: {stderr}",
    );

    let _ = fs::remove_dir_all(&root);
}

#[test]
fn abi_guard_skipped_with_env_opt_out() {
    let root = temp_root("guard_skipped");
    write_fixture(&root);

    let output = Command::new(nros_bin())
        .arg("generate-rust")
        .arg("--manifest")
        .arg(root.join("package.xml"))
        .arg("--output")
        .arg(root.join("generated"))
        .env("NROS_SKIP_VERSION_CHECK", "1")
        .output()
        .expect("spawn nros");

    let stderr = String::from_utf8_lossy(&output.stderr);
    let stdout = String::from_utf8_lossy(&output.stdout);
    let combined = format!("{stdout}\n{stderr}");

    // The ABI-guard mismatch message MUST NOT appear when the opt-out env
    // is set. We do NOT assert the verb's exit code — the synthetic
    // package.xml may legitimately fail downstream codegen.
    assert!(
        !combined.contains("ABI version mismatch"),
        "guard fired despite NROS_SKIP_VERSION_CHECK=1.\nstdout: {stdout}\nstderr: {stderr}",
    );
    // The bypass warning should surface so CI logs show the skip.
    assert!(
        combined.contains("ABI version guard bypassed"),
        "expected `bypassed` warning on stderr.\nstdout: {stdout}\nstderr: {stderr}",
    );

    let _ = fs::remove_dir_all(&root);
}
