    #[test]
    fn emit_register_dispatch_fn_shape_two_node_fixture() {
        // Phase 216 — framework Entry pkgs need a second emitted fn
        // that calls each Node pkg's `register_dispatch(&mut executor)`
        // to populate the Executor.dispatch_slots registry.
        let plan = plan_with_two_instances();
        let out = emit_run_plan_register_dispatch(&plan);
        assert!(
            out.contains(
                "pub fn run_plan_register_dispatch(\n    executor: &mut ::nros::Executor,\n) -> ::core::result::Result<(), ()> {",
            ),
            "expected `pub fn run_plan_register_dispatch(executor: &mut ::nros::Executor) -> Result<(), ()>` signature; got:\n{out}"
        );
        assert!(
            out.contains("::talker_pkg::register_dispatch(executor)?;"),
            "expected first Node's register_dispatch call; got:\n{out}"
        );
        // Dash-stripped crate ident for `listener-pkg`.
        assert!(
            out.contains("::listener_pkg::register_dispatch(executor)?;"),
            "expected second Node's register_dispatch call w/ dash→underscore projection; got:\n{out}"
        );
        // Plan-order preserved (talker before listener).
        let i_talker = out
            .find("::talker_pkg::register_dispatch")
            .expect("talker present");
        let i_listener = out
            .find("::listener_pkg::register_dispatch")
            .expect("listener present");
        assert!(
            i_talker < i_listener,
            "expected plan-order preserved; got:\n{out}"
        );
        assert!(
            out.contains("Ok(())\n}"),
            "expected trailing `Ok(())` + brace; got:\n{out}"
        );
    }

