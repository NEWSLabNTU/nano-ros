//! Emit `$OUT_DIR/run_plan.rs` from an [`NrosPlan`].
//!
//! The emitted file is `include!`d from the Entry pkg's `main.rs`.
//! It declares one fn:
//!
//! ```ignore
//! pub fn run_plan(
//!     runtime: &mut ::nros_platform::RuntimeCtx<'_>,
//! ) -> ::core::result::Result<(), ::nros_build::RuntimeError> {
//!     // For each plan.instances entry:
//!     //   ::<pkg_ident>::register(runtime).map_err(|_|
//!     //       ::nros_platform::RuntimeError::NodeRegister("<pkg>::<exec>"))?;
//!     Ok(())
//! }
//! ```
//!
//! The fn body deliberately keeps no per-instance state — it is a
//! flat sequence of register calls in plan-order (which the planner
//! already orders so producers come before consumers). Lifecycle
//! hooks / shared-state regions / param overlays are NOT yet emitted;
//! they live in the plan and will be folded in over follow-up
//! patches.

use core::fmt::Write as _;

use nros_cli_core::orchestration::plan::NrosPlan;

/// Render the body of `$OUT_DIR/run_plan.rs` for the given plan.
///
/// Returns a UTF-8 string the caller writes verbatim. Doc-comments
/// in the emitted code spell out the lineage (planner version,
/// system name, instance count) so a curious user can introspect
/// without re-running codegen.
pub fn emit_run_plan(plan: &NrosPlan) -> String {
    let mut out = String::with_capacity(4096);
    let _ = writeln!(
        out,
        "// AUTO-GENERATED by nros-build::emit_run_plan (Phase 212.N.4)."
    );
    let _ = writeln!(out, "// DO NOT EDIT — regenerated by Entry pkg build.rs.");
    let _ = writeln!(out, "//");
    let _ = writeln!(out, "// plan.version : {}", plan.version);
    let _ = writeln!(out, "// plan.system  : {}", plan.system);
    let _ = writeln!(out, "// instance count: {}", plan.instances.len());
    out.push('\n');

    out.push_str(
        "pub fn run_plan(\n    runtime: &mut ::nros_platform::RuntimeCtx<'_>,\n) -> ::core::result::Result<(), ::nros_platform::RuntimeError> {\n    let _ = runtime; // silence unused-warn on empty plans\n",
    );

    for inst in &plan.instances {
        // Match the planner's record-side pkg field (a launch <node
        // pkg="X" exec="Y"/> → record.node.package). The plan's
        // PlanInstance carries the unsanitised pkg name; here we
        // produce the dash-stripped ident that matches Rust's
        // crate-name → ident projection (`-` → `_`).
        //
        // Phase 212.M-F.19: §212.N.7 + §212.N.12 settled the
        // register-wrapper shape on `<pkg>::register` at the crate
        // root (no nested `<exec>` module). The symbol path is
        // therefore `::<pkg_ident>::register`; the error label keeps
        // the human-readable `<pkg>::<exec>` identity used in
        // diagnostics.
        let pkg_ident = pkg_ident_from(inst);
        let pkg_label = pkg_label_from(inst);
        let _ = writeln!(
            out,
            "    ::{pkg_ident}::register(runtime).map_err(|_| ::nros_platform::RuntimeError::NodeRegister({pkg_label:?}))?;"
        );
    }

    out.push_str("    Ok(())\n}\n");
    out
}

/// phase-267 W1b/S1 — the cross-RMW bridge entry fns for a `[[bridge]]` plan, or
/// `None` for an ordinary single-session plan.
///
/// This is the **carrier** that brings the (proven, unit-tested) RFC-0009 bridge
/// relay codegen into the LIVE native-Rust entry path: `nros-build` already holds
/// the full [`NrosPlan`] (with `build.transports` + `plan.bridges`, populated by
/// the planner), so it can emit the bridge support set
/// ([`nros_cli_core::orchestration::generate::render_bridge_entry_fns`]:
/// `SESSION_SPECS` + `register_backends` + `build_executor_bridge` +
/// `register_bridges`). S2 wires the `nros::main!` OwnedSpin entry to splice this
/// + drive `Executor::open_multi`; until then this is reachable but unused (no
/// behaviour change for non-bridge plans, which get `None`).
pub fn emit_bridge_entry_fns(plan: &NrosPlan) -> Option<String> {
    nros_cli_core::orchestration::generate::render_bridge_entry_fns(plan)
}

/// Render `run_plan_register_dispatch(executor)` — the framework-target
/// (RTIC + Embassy, Phase 216) sibling of [`emit_run_plan`].
///
/// Each Node pkg's `nros::node!()` macro emits a
/// `pub fn register_dispatch(executor: &mut ::nros::Executor) -> Result<(), ()>`
/// that populates `Executor.dispatch_slots`. Framework Entry pkgs
/// driven by a launch.xml-derived plan can call this fn from `#[init]`
/// instead of having to declare each Node in the proc-macro's
/// `node_pkgs = [...]` list.
///
/// The fn is additive — the existing `nros::main!()` `node_pkgs = [...]`
/// path is unchanged. Consumers opt in by calling
/// `run_plan_register_dispatch(&mut executor)` from their `#[init]`
/// body.
pub fn emit_run_plan_register_dispatch(plan: &NrosPlan) -> String {
    let mut out = String::with_capacity(2048);
    let _ = writeln!(
        out,
        "// AUTO-GENERATED by nros-build::emit_run_plan_register_dispatch (Phase 216)."
    );
    let _ = writeln!(out, "// DO NOT EDIT — regenerated by Entry pkg build.rs.");
    let _ = writeln!(out, "//");
    let _ = writeln!(out, "// plan.version : {}", plan.version);
    let _ = writeln!(out, "// plan.system  : {}", plan.system);
    let _ = writeln!(out, "// instance count: {}", plan.instances.len());
    out.push('\n');

    out.push_str(
        "pub fn run_plan_register_dispatch(\n    executor: &mut ::nros::Executor,\n) -> ::core::result::Result<(), ()> {\n    let _ = executor; // silence unused-warn on empty plans\n",
    );

    for inst in &plan.instances {
        // Same crate-name → ident projection as `emit_run_plan` — the
        // `<pkg>::register_dispatch` symbol lives at the crate root
        // (Phase 216 final wave, 67582608a).
        let pkg_ident = pkg_ident_from(inst);
        let _ = writeln!(out, "    ::{pkg_ident}::register_dispatch(executor)?;");
    }

    out.push_str("    Ok(())\n}\n");
    out
}

fn pkg_ident_from(inst: &nros_cli_core::orchestration::plan::PlanInstance) -> String {
    // Phase 212.M-F.19: the Rust symbol path is `::<pkg>::register`
    // where `<pkg>` is the *crate name* (post-`-`→`_` projection).
    // Prefer the planner's `package` field (the unambiguous crate
    // name); fall back across older planner versions that only set
    // `pkg` / `name`. We explicitly do NOT fall back to `component`
    // here — post-§212.M-F.17 the planner serializes `component` as
    // the combined `"<pkg>::<exec>"` identity, which would generate
    // a non-existent `::<pkg>::<exec>::register` symbol.
    let raw = instance_field(inst, &["package", "pkg", "name"]);
    raw.replace('-', "_")
}

fn pkg_label_from(inst: &nros_cli_core::orchestration::plan::PlanInstance) -> String {
    // Diagnostics keep the `<pkg>::<exec>` human identity (so a
    // RuntimeError::NodeRegister bubble carries enough context to
    // disambiguate two executables in the same crate). The planner's
    // `component` field already carries that combined identity post-
    // §212.M-F.17; fall through to bare-pkg when older planners only
    // expose the crate name.
    let combined = instance_field(inst, &["component"]);
    if !combined.is_empty() && combined != "unknown" {
        return combined;
    }
    let pkg = instance_field(inst, &["package", "pkg", "name"]);
    let exec = instance_field(inst, &["executable", "exec"]);
    if !exec.is_empty() && exec != "unknown" && pkg != exec {
        format!("{pkg}::{exec}")
    } else {
        pkg
    }
}

fn instance_field(
    inst: &nros_cli_core::orchestration::plan::PlanInstance,
    keys: &[&str],
) -> String {
    // Round-trip through serde_json::Value so the emitter doesn't
    // require a knowledge of `PlanInstance`'s precise field layout
    // (which has churned across planner versions). Walk the keys in
    // priority order; return the first hit.
    let v = serde_json::to_value(inst).unwrap_or(serde_json::Value::Null);
    for key in keys {
        if let Some(s) = v.get(*key).and_then(|x| x.as_str()) {
            return s.to_string();
        }
    }
    "unknown".to_string()
}

