template <typename M>
Result Node::create_subscription(Subscription<M>& out, const char* topic, const QoS& qos) {
    if (!initialized_) return Result(ErrorCode::NotInitialized);
    nros_cpp_qos_t ffi_qos;
    ffi_qos.reliability = static_cast<nros_cpp_qos_reliability_t>(qos.reliability_raw());
    ffi_qos.durability = static_cast<nros_cpp_qos_durability_t>(qos.durability_raw());
    ffi_qos.history = static_cast<nros_cpp_qos_history_t>(qos.history_raw());
    ffi_qos.liveliness_kind = static_cast<nros_cpp_qos_liveliness_t>(qos.liveliness_raw());
    ffi_qos.depth = qos.depth();
    ffi_qos.deadline_ms = qos.deadline_ms();
    ffi_qos.lifespan_ms = qos.lifespan_ms();
    ffi_qos.liveliness_lease_ms = qos.liveliness_lease_ms();
    ffi_qos.avoid_ros_namespace_conventions = qos.avoid_ros_namespace_conventions() ? 1 : 0;
    nros_cpp_ret_t ret = nros_cpp_subscription_create(&handle_, topic, M::TYPE_NAME, M::TYPE_HASH,
                                                      ffi_qos, out.storage_);
    if (ret == 0) {
        // Phase 87.6: topic name lives C++-side now.
        size_t topic_len = 0;
        while (topic[topic_len] != '\0' && topic_len + 1 < sizeof(out.topic_name_)) {
            out.topic_name_[topic_len] = topic[topic_len];
            ++topic_len;
        }
        out.topic_name_[topic_len] = '\0';
        out.initialized_ = true;
    }
    return Result(ret);
}

/// Phase 189.M3.1 — named-options overload. Delegates to the qos-only
/// create, then lowers the non-QoS axes:
///
///  * `sched_context` — when set (`!= SCHED_CONTEXT_UNSET`) **and** the
///    created subscription exposes a bindable executor `HandleId`,
///    create-then-bind via `nros_cpp_bind_handle_to_sched_context`; a
///    failing bind tears the subscription back down and surfaces the FFI
///    error. The current thin-wrapper subscription has no bindable handle
///    (see `Subscription<M>::has_sched_handle()`), so the bind is skipped
///    until a handle-returning create FFI lands (tracked with M3.4); the
///    field is honoured transparently once that exists.
///  * `message_info` — reserved (M3.4); ignored today.
template <typename M>
Result Node::create_subscription(Subscription<M>& out, const char* topic, const QoS& qos,
                                 const SubscriptionOptions& options) {
    Result r = create_subscription<M>(out, topic, qos);
    if (!r.ok()) return r;

    // TODO(M3.4): honour options.message_info via the with-info arena path.

    if (options.sched_context != SCHED_CONTEXT_UNSET && out.has_sched_handle()) {
        nros_cpp_ret_t bind = nros_cpp_bind_handle_to_sched_context(
            executor_handle_, out.sched_handle_id(), static_cast<uint8_t>(options.sched_context));
        if (bind != 0) {
            // Roll back so the caller doesn't observe a half-configured
            // entity. Destructor-on-out would also fire, but explicit
            // teardown keeps the returned error authoritative.
            nros_cpp_subscription_destroy(out.storage_);
            out.initialized_ = false;
            return Result(bind);
        }
    }
    return Result::success();
}

/// Phase 123.B.4 — value-returning subscription factory. Pairs
/// with `make_publisher` so the full pub/sub create dance is
/// expressible as a chain of `auto`-typed factories.
template <typename M>
inline Expected<Subscription<M>> make_subscription(Node& node, const char* topic,
                                                   const QoS& qos = QoS::default_profile()) {
    Subscription<M> s;
    Result r = node.create_subscription<M>(s, topic, qos);
    if (!r.ok()) return Expected<Subscription<M>>::error(r);
    return Expected<Subscription<M>>::ok(std::move(s));
}

