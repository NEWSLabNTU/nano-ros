const _: () = assert!(
    u64s_for::<nros_node::lifecycle::LifecyclePollingNodeCtx>()
        <= crate::constants::NROS_LIFECYCLE_CTX_OPAQUE_U64S,
    "NROS_LIFECYCLE_CTX_OPAQUE_U64S too small вЂ” \
     bump constants::NROS_LIFECYCLE_CTX_OPAQUE_U64S and types.h to match"
);

// в”Ђв”Ђ Action Server в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
//
// ACTION_SERVER_INTERNAL_OPAQUE_U64S is defined in `crate::config`
// (generated by build.rs) as a conservative upper bound that holds
// across all supported target architectures. The compile-time assertion
// below verifies that the upper bound is sufficient for the actual Rust
// type. If it fails, increase `action_server_storage_bytes` in build.rs.
//
// Phase 87.5: `ActionClientInternal`, `ServiceClientInternal`, and
// `ServiceServerInternal` are now `#[repr(C)]` and embedded directly in
// their outer `nros_*_t` types вЂ” no opaque storage, no upper-bound
// assertion needed. `ActionServerInternal` still uses the opaque pattern
// because its `Option<ActionServerRawHandle>` field can't be made
// `#[repr(C)]` without a separate niche-free rewrite.

#[cfg(any(feature = "rmw-zenoh", feature = "rmw-xrce"))]
const _: () = assert!(
    u64s_for::<crate::action::ActionServerInternal>()
        <= crate::config::ACTION_SERVER_INTERNAL_OPAQUE_U64S,
    "ACTION_SERVER_INTERNAL_OPAQUE_U64S too small вЂ” \
     increase action_server_storage_bytes in build.rs"
);
