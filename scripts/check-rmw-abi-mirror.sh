#!/usr/bin/env bash
#
# Issue #239 — field-parity drift gate for the RMW C ABI hand-mirror.
#
# The RMW vtable (`nros_rmw_vtable_t` ↔ `NrosRmwVtable`) and every
# `nros_rmw_*_t` entity struct (`NrosRmwQos`, `NrosRmwSession`, …) are
# HAND-KEPT Rust↔C mirrors. The #238 abi_layout size/align asserts pin
# widths, but a same-width field split/rename/reorder slips through them
# (exactly how the QoS `tx_express` split landed Rust-only). This gate
# compares the ORDERED member-name lists on both sides:
#
#   - vtable: every top-level fn-pointer slot name, in declaration order
#     (paren-depth-aware so nested callback parameters like `(*cb)` inside
#     a slot's signature don't count);
#   - entity structs: every field name, in declaration order.
#
# Signature/width drift within a same-named slot remains pinned by the
# #238 asserts (`abi_layout` const block + `abi_layout_check.c`) plus the
# in-tree C consumers compiling against the header.
#
# Hooked from `just check` next to check-ffi-struct-mirrors.sh (the cpp
# twin) and check-platform-abi-mirror.sh (the platform 3-way gate).

set -euo pipefail
cd "$(dirname "$0")/.."

CFFI="packages/core/nros-rmw-cffi"
RUST="$CFFI/src/lib.rs"

python3 - "$CFFI" "$RUST" << 'PYEOF'
import re
import sys

cffi_dir, rust_path = sys.argv[1], sys.argv[2]
rust = open(rust_path).read()
fail = False


def strip_c_comments(src: str) -> str:
    src = re.sub(r"/\*.*?\*/", "", src, flags=re.S)
    return re.sub(r"//[^\n]*", "", src)


def c_struct_body(src: str, tag: str) -> str:
    m = re.search(
        r"typedef struct %s \{(.*?)\} %s;" % (re.escape(tag), re.escape(tag)), src, re.S
    )
    if not m:
        raise SystemExit(f"error: C struct {tag} not found")
    return m.group(1)


def rust_struct_fields(name: str) -> list[str]:
    m = re.search(r"pub struct %s \{(.*?)\n\}" % re.escape(name), rust, re.S)
    if not m:
        raise SystemExit(f"error: Rust struct {name} not found")
    return re.findall(r"\n    pub ([A-Za-z_0-9]+):", m.group(1))


def c_vtable_slots(body: str) -> list[str]:
    """Top-level `(*name)` member names, paren-depth-aware (skip cb params)."""
    depth, slots, i = 0, [], 0
    while i < len(body):
        ch = body[i]
        if ch == "(":
            if depth == 0:
                mm = re.match(r"\(\s*\*\s*([A-Za-z_0-9]+)\s*\)", body[i:])
                if mm:
                    slots.append(mm.group(1))
            depth += 1
        elif ch == ")":
            depth -= 1
        i += 1
    return slots


def c_plain_fields(body: str) -> list[str]:
    """Field names of a plain (non-fn-pointer) struct, incl. array fields."""
    fields = []
    for stmt in body.split(";"):
        stmt = stmt.strip()
        if not stmt:
            continue
        mm = re.match(
            r"(?:const\s+)?[A-Za-z_][A-Za-z_0-9 ]*[\s\*]+([A-Za-z_0-9]+)"
            r"(\[[A-Za-z_0-9]+\])?$",
            stmt,
        )
        if mm:
            fields.append(mm.group(1))
    return fields


def compare(kind: str, c_names: list[str], r_names: list[str]) -> None:
    global fail
    if c_names == r_names:
        print(f"{kind} clean: {len(c_names)} members match")
        return
    fail = True
    print(f"ERROR: {kind} MIRROR DRIFT (C {len(c_names)} vs Rust {len(r_names)}):")
    for i in range(max(len(c_names), len(r_names))):
        c = c_names[i] if i < len(c_names) else "<missing>"
        r = r_names[i] if i < len(r_names) else "<missing>"
        if c != r:
            print(f"  [{i}] C `{c}` vs Rust `{r}`")


vtable_hdr = strip_c_comments(open(f"{cffi_dir}/include/nros/rmw_vtable.h").read())
entity_hdr = strip_c_comments(open(f"{cffi_dir}/include/nros/rmw_entity.h").read())
event_hdr = strip_c_comments(open(f"{cffi_dir}/include/nros/rmw_event.h").read())

compare(
    "nros_rmw_vtable_t",
    c_vtable_slots(c_struct_body(vtable_hdr, "nros_rmw_vtable_t")),
    rust_struct_fields("NrosRmwVtable"),
)

ENTITY_PAIRS = [
    ("nros_rmw_qos_t", "NrosRmwQos", entity_hdr),
    ("nros_rmw_session_t", "NrosRmwSession", entity_hdr),
    ("nros_rmw_publisher_t", "NrosRmwPublisher", entity_hdr),
    ("nros_rmw_subscriber_t", "NrosRmwSubscriber", entity_hdr),
    ("nros_rmw_service_server_t", "NrosRmwServiceServer", entity_hdr),
    ("nros_rmw_service_client_t", "NrosRmwServiceClient", entity_hdr),
    ("nros_rmw_count_status_t", "NrosRmwCountStatus", event_hdr),
    ("nros_rmw_liveliness_changed_status_t", "NrosRmwLivelinessChangedStatus", event_hdr),
]
for c_tag, r_name, hdr in ENTITY_PAIRS:
    compare(c_tag, c_plain_fields(c_struct_body(hdr, c_tag)), rust_struct_fields(r_name))

if fail:
    sys.exit(1)
print(f"rmw C ABI mirror clean: vtable + {len(ENTITY_PAIRS)} entity structs")
PYEOF

# Compiler-derived offset/size parity (the half text parity can't see:
# same-name same-total-size width changes). C offsetof exports vs Rust
# offset_of! — tests/abi_offsets.rs, generated by
# scripts/gen-rmw-abi-offsets.py. Cargo-cached, so cheap when unchanged.
if command -v cargo-nextest >/dev/null 2>&1; then
    cargo nextest run -p nros-rmw-cffi --features c-stub-test \
        --test abi_offsets --status-level fail 2>/dev/null | tail -1
else
    cargo test -q -p nros-rmw-cffi --features c-stub-test --test abi_offsets
fi
