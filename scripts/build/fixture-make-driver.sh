#!/usr/bin/env bash
# Generate and run a temporary make graph for manifest-driven fixture leaves.
#
# Phase 226 skeleton only: this is deliberately not wired into just recipes yet.
# For now it emits native fixture groups and calls the existing grouped builder
# (scripts/build/fixtures-build.sh <platform> <lang> [rmw]) as each make leaf.
set -euo pipefail

usage() {
    cat >&2 <<'EOF'
usage: scripts/build/fixture-make-driver.sh [--dry-run] [--keep] <platform|all>

Generates a temporary makefile, joblog, and leaf logs under build/fixture-make-driver/.
Current skeleton scope: native manifest-driven fixture groups only.

Options:
  --dry-run, -n   generate and print the make command without executing it
  --keep         keep the generated makefile after a successful run
EOF
}

dry_run=0
keep=0
scope=""
while [ "$#" -gt 0 ]; do
    case "$1" in
        --dry-run|-n)
            dry_run=1
            ;;
        --keep)
            keep=1
            ;;
        --help|-h)
            usage
            exit 0
            ;;
        -*)
            usage
            exit 2
            ;;
        *)
            if [ -n "$scope" ]; then
                usage
                exit 2
            fi
            scope="$1"
            ;;
    esac
    shift
done

if [ -z "$scope" ]; then
    usage
    exit 2
fi

case "$scope" in
    native|all)
        ;;
    *)
        echo "fixture-make-driver: unsupported platform for skeleton: $scope" >&2
        echo "fixture-make-driver: current scope is native manifest-driven leaves only" >&2
        exit 2
        ;;
esac

repo_root="$(pwd)"
if [ ! -f "$repo_root/examples/fixtures.toml" ] || [ ! -x "$repo_root/scripts/build/fixtures-build.sh" ]; then
    echo "fixture-make-driver: run from the nano-ros repository root" >&2
    exit 2
fi

stamp="$(date +%Y%m%d-%H%M%S)-$$"
work_root="$repo_root/build/fixture-make-driver"
log_dir="$work_root/logs/$stamp"
status_dir="$work_root/status/$stamp"
joblog="$work_root/joblog-$stamp.tsv"
makefile="$work_root/fixture-$stamp.mk"
mkdir -p "$log_dir" "$status_dir"
printf 'target\tplatform\tlang\trmw\tstatus\tstart_epoch\tend_epoch\tduration_s\tlog\n' >"$joblog"

group_file="$work_root/groups-$stamp.tsv"
mkdir -p "$work_root"
python3 - "$repo_root/examples/fixtures.toml" >"$group_file" <<'PY'
import sys
from pathlib import Path

try:
    import tomllib
except ModuleNotFoundError:
    import tomli as tomllib

manifest = Path(sys.argv[1])
with manifest.open("rb") as f:
    rows = tomllib.load(f).get("fixture", [])

seen = set()
for row in rows:
    if row.get("platform") != "native":
        continue
    lang = row.get("lang") or "rust"
    rmw = row.get("rmw") or ""
    key = (lang, rmw)
    if key in seen:
        continue
    seen.add(key)
    print(f"{lang}\t{rmw}")
PY

if [ ! -s "$group_file" ]; then
    echo "fixture-make-driver: no native fixture groups found in examples/fixtures.toml" >&2
    exit 1
fi

targets=()
leaf_details=()
{
    printf '# Generated by scripts/build/fixture-make-driver.sh at %s\n' "$stamp"
    printf 'SHELL := /bin/bash\n'
    printf '.SHELLFLAGS := -eu -o pipefail -c\n'
    printf '.DELETE_ON_ERROR:\n'
    printf '.PHONY: all'
    while IFS=$'\t' read -r lang rmw; do
        [ -n "$lang" ] || continue
        name="native-${lang}${rmw:+-${rmw}}"
        name="${name//_/-}"
        target="fixture-${name}"
        targets+=("$target")
        if [ -n "$rmw" ]; then
            leaf_details+=("  target=$target platform=native lang=$lang rmw=$rmw log=$log_dir/$name.log status=$status_dir/$name.status command=scripts/build/fixtures-build.sh native $lang $rmw")
        else
            leaf_details+=("  target=$target platform=native lang=$lang rmw=<none> log=$log_dir/$name.log status=$status_dir/$name.status command=scripts/build/fixtures-build.sh native $lang")
        fi
        printf ' %s' "$target"
    done <"$group_file"
    printf '\n\n'
    printf 'all:'
    for target in "${targets[@]}"; do
        printf ' %s' "$target"
    done
    printf '\n\n'

    while IFS=$'\t' read -r lang rmw; do
        [ -n "$lang" ] || continue
        name="native-${lang}${rmw:+-${rmw}}"
        name="${name//_/-}"
        target="fixture-${name}"
        log="$log_dir/$name.log"
        status_file="$status_dir/$name.status"
        printf '%s:\n' "$target"
        printf '\t+@echo "fixture: native %s%s"\n' "$lang" "${rmw:+ $rmw}"
        if [ -n "$rmw" ]; then
            printf '\t+@start=$$(date +%%s); status=0; echo "running" >%s; NROS_JOBSERVER=1 scripts/build/fixtures-build.sh native %s %s >%s 2>&1 || status=$$?; end=$$(date +%%s); duration=$$((end - start)); if [ "$$status" -eq 0 ]; then state=ok; else state=fail; fi; printf "target=%%s\\nplatform=native\\nlang=%%s\\nrmw=%%s\\nstatus=%%s\\nstart_epoch=%%s\\nend_epoch=%%s\\nduration_s=%%s\\nlog=%%s\\n" "%s" "%s" "%s" "$$state" "$$start" "$$end" "$$duration" "%s" >%s; printf "%%s\\tnative\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\n" "%s" "%s" "%s" "$$state" "$$start" "$$end" "$$duration" "%s" >>%s; if [ "$$status" -ne 0 ]; then echo "fixture-make-driver: %s failed; tail of %s:" >&2; tail -n "$${NROS_FIXTURE_FAIL_TAIL:-80}" %s >&2 || true; exit "$$status"; fi\n' "$status_file" "$lang" "$rmw" "$log" "$target" "$lang" "$rmw" "$log" "$status_file" "$target" "$lang" "$rmw" "$log" "$joblog" "$target" "$log" "$log"
        else
            printf '\t+@start=$$(date +%%s); status=0; echo "running" >%s; NROS_JOBSERVER=1 scripts/build/fixtures-build.sh native %s >%s 2>&1 || status=$$?; end=$$(date +%%s); duration=$$((end - start)); if [ "$$status" -eq 0 ]; then state=ok; else state=fail; fi; printf "target=%%s\\nplatform=native\\nlang=%%s\\nrmw=%%s\\nstatus=%%s\\nstart_epoch=%%s\\nend_epoch=%%s\\nduration_s=%%s\\nlog=%%s\\n" "%s" "%s" "" "$$state" "$$start" "$$end" "$$duration" "%s" >%s; printf "%%s\\tnative\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\t%%s\\n" "%s" "%s" "" "$$state" "$$start" "$$end" "$$duration" "%s" >>%s; if [ "$$status" -ne 0 ]; then echo "fixture-make-driver: %s failed; tail of %s:" >&2; tail -n "$${NROS_FIXTURE_FAIL_TAIL:-80}" %s >&2 || true; exit "$$status"; fi\n' "$status_file" "$lang" "$log" "$target" "$lang" "$log" "$status_file" "$target" "$lang" "$log" "$joblog" "$target" "$log" "$log"
        fi
    done <"$group_file"
} >"$makefile"

rm -f "$group_file"

make_bin="make"
make_args=()
jobs="${NROS_BUILD_JOBS:-$(nproc 2>/dev/null || echo 8)}"
if [ -x "$repo_root/third-party/make/make" ] && \
   "$repo_root/third-party/make/make" --version | head -1 | grep -q "4.4" && \
   [ -x "$repo_root/third-party/ninja/ninja" ]; then
    make_bin="$repo_root/third-party/make/make"
    make_args=(-j"$jobs" --jobserver-style=fifo)
    export PATH="$repo_root/third-party/make:$repo_root/third-party/ninja:$PATH"
    echo "fixture-make-driver: using pinned fifo make"
else
    make_args=(-j"$jobs")
    echo "fixture-make-driver: pinned fifo tools not found; using ordinary make fallback"
fi

echo "fixture-make-driver: makefile=$makefile"
echo "fixture-make-driver: log-dir=$log_dir"
echo "fixture-make-driver: status-dir=$status_dir"
echo "fixture-make-driver: joblog=$joblog"
echo "fixture-make-driver: targets=${targets[*]}"
echo "fixture-make-driver: command=$make_bin ${make_args[*]} -f $makefile"

if [ "$dry_run" = "1" ]; then
    echo "fixture-make-driver: leaves:"
    for detail in "${leaf_details[@]}"; do
        echo "$detail"
    done
    echo "fixture-make-driver: dry run; not executing make"
    exit 0
fi

env -u MAKEFLAGS -u CARGO_MAKEFLAGS \
    NROS_BUILD_LOG_DIR="$log_dir" \
    "$make_bin" "${make_args[@]}" -f "$makefile"

echo "fixture-make-driver: joblog=$joblog"

if [ "$keep" != "1" ]; then
    rm -f "$makefile"
else
    echo "fixture-make-driver: kept makefile=$makefile"
fi
