#!/usr/bin/env bash
# Generate and run a temporary make graph for manifest-driven fixture leaves.
#
# Phase 226 skeleton only: this is deliberately not wired into just recipes yet.
# For now it emits native fixture groups and calls the existing grouped builder
# (scripts/build/fixtures-build.sh <platform> <lang> [rmw]) as each make leaf.
set -euo pipefail

usage() {
    cat >&2 <<'EOF'
usage: scripts/build/fixture-make-driver.sh [--dry-run] [--keep] <platform|all>

Generates a temporary makefile and log directory under build/fixture-make-driver/.
Current skeleton scope: native manifest-driven fixture groups only.

Options:
  --dry-run, -n   generate and print the make command without executing it
  --keep         keep the generated makefile after a successful run
EOF
}

dry_run=0
keep=0
scope=""
while [ "$#" -gt 0 ]; do
    case "$1" in
        --dry-run|-n)
            dry_run=1
            ;;
        --keep)
            keep=1
            ;;
        --help|-h)
            usage
            exit 0
            ;;
        -*)
            usage
            exit 2
            ;;
        *)
            if [ -n "$scope" ]; then
                usage
                exit 2
            fi
            scope="$1"
            ;;
    esac
    shift
done

if [ -z "$scope" ]; then
    usage
    exit 2
fi

case "$scope" in
    native|all)
        ;;
    *)
        echo "fixture-make-driver: unsupported platform for skeleton: $scope" >&2
        echo "fixture-make-driver: current scope is native manifest-driven leaves only" >&2
        exit 2
        ;;
esac

repo_root="$(pwd)"
if [ ! -f "$repo_root/examples/fixtures.toml" ] || [ ! -x "$repo_root/scripts/build/fixtures-build.sh" ]; then
    echo "fixture-make-driver: run from the nano-ros repository root" >&2
    exit 2
fi

stamp="$(date +%Y%m%d-%H%M%S)-$$"
work_root="$repo_root/build/fixture-make-driver"
log_dir="$work_root/logs/$stamp"
makefile="$work_root/fixture-$stamp.mk"
mkdir -p "$log_dir"

group_file="$work_root/groups-$stamp.tsv"
mkdir -p "$work_root"
python3 - "$repo_root/examples/fixtures.toml" >"$group_file" <<'PY'
import sys
from pathlib import Path

try:
    import tomllib
except ModuleNotFoundError:
    import tomli as tomllib

manifest = Path(sys.argv[1])
with manifest.open("rb") as f:
    rows = tomllib.load(f).get("fixture", [])

seen = set()
for row in rows:
    if row.get("platform") != "native":
        continue
    lang = row.get("lang") or "rust"
    rmw = row.get("rmw") or ""
    key = (lang, rmw)
    if key in seen:
        continue
    seen.add(key)
    print(f"{lang}\t{rmw}")
PY

if [ ! -s "$group_file" ]; then
    echo "fixture-make-driver: no native fixture groups found in examples/fixtures.toml" >&2
    exit 1
fi

targets=()
{
    printf '# Generated by scripts/build/fixture-make-driver.sh at %s\n' "$stamp"
    printf 'SHELL := /bin/bash\n'
    printf '.DELETE_ON_ERROR:\n'
    printf '.PHONY: all'
    while IFS=$'\t' read -r lang rmw; do
        [ -n "$lang" ] || continue
        name="native-${lang}${rmw:+-${rmw}}"
        name="${name//_/-}"
        target="fixture-${name}"
        targets+=("$target")
        printf ' %s' "$target"
    done <"$group_file"
    printf '\n\n'
    printf 'all:'
    for target in "${targets[@]}"; do
        printf ' %s' "$target"
    done
    printf '\n\n'

    while IFS=$'\t' read -r lang rmw; do
        [ -n "$lang" ] || continue
        name="native-${lang}${rmw:+-${rmw}}"
        name="${name//_/-}"
        target="fixture-${name}"
        log="$log_dir/$name.log"
        printf '%s:\n' "$target"
        printf '\t+@echo "fixture: native %s%s"\n' "$lang" "${rmw:+ $rmw}"
        if [ -n "$rmw" ]; then
            printf '\t+@NROS_JOBSERVER=1 scripts/build/fixtures-build.sh native %s %s >%s 2>&1\n' "$lang" "$rmw" "$log"
        else
            printf '\t+@NROS_JOBSERVER=1 scripts/build/fixtures-build.sh native %s >%s 2>&1\n' "$lang" "$log"
        fi
    done <"$group_file"
} >"$makefile"

rm -f "$group_file"

make_bin="make"
make_args=()
jobs="${NROS_BUILD_JOBS:-$(nproc 2>/dev/null || echo 8)}"
if [ -x "$repo_root/third-party/make/make" ] && \
   "$repo_root/third-party/make/make" --version | head -1 | grep -q "4.4" && \
   [ -x "$repo_root/third-party/ninja/ninja" ]; then
    make_bin="$repo_root/third-party/make/make"
    make_args=(-j"$jobs" --jobserver-style=fifo)
    export PATH="$repo_root/third-party/make:$repo_root/third-party/ninja:$PATH"
    echo "fixture-make-driver: using pinned fifo make"
else
    make_args=(-j"$jobs")
    echo "fixture-make-driver: pinned fifo tools not found; using ordinary make fallback"
fi

echo "fixture-make-driver: makefile=$makefile"
echo "fixture-make-driver: log-dir=$log_dir"
echo "fixture-make-driver: targets=${targets[*]}"
echo "fixture-make-driver: command=$make_bin ${make_args[*]} -f $makefile"

if [ "$dry_run" = "1" ]; then
    echo "fixture-make-driver: dry run; not executing make"
    exit 0
fi

env -u MAKEFLAGS -u CARGO_MAKEFLAGS \
    NROS_BUILD_LOG_DIR="$log_dir" \
    "$make_bin" "${make_args[@]}" -f "$makefile"

if [ "$keep" != "1" ]; then
    rm -f "$makefile"
else
    echo "fixture-make-driver: kept makefile=$makefile"
fi
