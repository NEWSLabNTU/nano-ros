#!/usr/bin/env bash
#
# nros ESP-IDF workspace setup.
#
# Installs Espressif's IDF SDK into $NROS_ESP_IDF_WORKSPACE, defaulting to
# $repo/esp-idf-workspace/esp-idf/ (gitignored). Distinct from the default
# `just esp32 setup` (which only installs Espressif's QEMU fork — the
# project's main ESP32 path is `esp-hal` bare-metal, not IDF).
#
# This workspace is opt-in for users who want to drive the
# `nros-platform-esp-idf` C port through an actual IDF build (target
# chips: esp32c3, esp32s3, esp32). It is NOT pulled by `just esp32 setup`.
#
# Usage:
#   ./scripts/esp_idf/setup.sh [--branch v5.3 | --tag <ref>] [--targets "esp32c3,esp32s3"]
#
# Env overrides:
#   NROS_ESP_IDF_WORKSPACE   Path to install IDF at (default:
#                            $repo/esp-idf-workspace/esp-idf)
#   ESP_IDF_REF              Branch / tag / commit (default: v5.3)
#   ESP_IDF_TARGETS          Comma-separated target chips (default: esp32c3)
#
# Prerequisites:
#   - python3 (3.8+), pip, git
#   - cmake, ninja-build
#   - libusb-1.0 (Linux)
#
# After install, source the env file:
#   source esp-idf-workspace/env.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
NANO_ROS_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"

WORKSPACE_DIR="${NROS_ESP_IDF_WORKSPACE:-$NANO_ROS_ROOT/esp-idf-workspace/esp-idf}"
ESP_IDF_REF="${ESP_IDF_REF:-v5.3}"
ESP_IDF_TARGETS="${ESP_IDF_TARGETS:-esp32c3}"

# Parse flags.
while [[ $# -gt 0 ]]; do
    case "$1" in
        --branch|--tag) ESP_IDF_REF="$2"; shift 2 ;;
        --targets)      ESP_IDF_TARGETS="$2"; shift 2 ;;
        -h|--help)
            sed -n '3,29p' "${BASH_SOURCE[0]}"
            exit 0
            ;;
        *) echo "unknown flag: $1" >&2; exit 1 ;;
    esac
done

echo "ESP-IDF workspace: $WORKSPACE_DIR"
echo "ESP-IDF ref:       $ESP_IDF_REF"
echo "Targets:           $ESP_IDF_TARGETS"

# 1. Clone (or update) ESP-IDF.
if [[ -d "$WORKSPACE_DIR/.git" ]]; then
    echo "==> esp-idf already cloned; fetching $ESP_IDF_REF"
    # NOTE: `fetch origin <ref>:<ref>` writes the destination into
    # `refs/heads/<ref>` (a branch). When `$ESP_IDF_REF` is an
    # annotated tag (e.g. v5.3), the tag object is a non-commit and
    # git refuses with "trying to write non-commit object". Fetch
    # the ref + its tags into FETCH_HEAD / refs/tags (no forced
    # branch destination), then check it out — works for both tags
    # and branches.
    git -C "$WORKSPACE_DIR" fetch --depth 1 --tags origin "$ESP_IDF_REF"
    git -C "$WORKSPACE_DIR" checkout "$ESP_IDF_REF"
else
    echo "==> cloning esp-idf @$ESP_IDF_REF"
    mkdir -p "$(dirname "$WORKSPACE_DIR")"
    git clone --depth 1 --branch "$ESP_IDF_REF" --recurse-submodules --shallow-submodules \
        https://github.com/espressif/esp-idf.git "$WORKSPACE_DIR"
fi

# 2. Run IDF's installer (downloads + sets up toolchains in ~/.espressif/).
echo "==> running esp-idf install.sh for targets: $ESP_IDF_TARGETS"
cd "$WORKSPACE_DIR"
./install.sh "$ESP_IDF_TARGETS"

# 3. Write an env.sh shim so callers can source one file.
ENV_SHIM="$NANO_ROS_ROOT/esp-idf-workspace/env.sh"
cat > "$ENV_SHIM" <<EOF
# Auto-generated by scripts/esp_idf/setup.sh
# Source from nano-ros root: \`source esp-idf-workspace/env.sh\`
export IDF_PATH="$WORKSPACE_DIR"
. "\$IDF_PATH/export.sh"
EOF
chmod +x "$ENV_SHIM"

echo ""
echo "ESP-IDF install complete."
echo "  IDF_PATH : $WORKSPACE_DIR"
echo "  Targets  : $ESP_IDF_TARGETS"
echo ""
echo "Activate the IDF environment in a shell:"
echo "  source esp-idf-workspace/env.sh"
echo ""
echo "Then run a smoke build:"
echo "  just esp_idf build-c-port"
